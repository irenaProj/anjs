function pattern(patternInitVal){
    var pattern = patternInitVal;    

    return{
        clear: function() {
            var len = patternInitVal.length;;

            for(var i = 0; i < len; i++){
                for(var k = 0; k < len; k++){
                    pattern[i][k] = 0;
                }
            }    
        },
        createEmptyLines: function() {
            var mat = new Array(2);
            var len = patternInitVal.length;
            
            mat[0] = new Array(len); 
            mat[1] = new Array(len);            
            
            return mat;
        },
        getRows: function() {return patternInitVal.length;},
        getColumns: function() {return patternInitVal[0].length;},
        getCell: function(i, k) {return pattern[i][k];},
        setCell: function(i, k, data) {pattern[i][k] = data;}
    };
}

function createMainPattern(){
    var len = MAIN_PATTERN_SIDE_LEN;
    var mat = new Array(len);
    
    for(var i = 0; i < len; i++){
        mat[i] = new Array(len);
    }
    
    for(var i = 0; i < len; i++){
        for(var k = 0; k < len; k++){
            mat[i][k] = 0;
        }
    }    
    
    return pattern(mat);
}

function resetMain(pattern){
    pattern.clear();
}

function copyPatternToMain(mainPattern, pattern, c, r){
    var rows = pattern.getRows();
    var cols = pattern.getColumns();
    var ctx = getCtx(MAIN_PATTERN_CANVAS_NAME);
    ctx.fillStyle = "#00f";

    if (c < 0 || r < 0 ||
        r + rows >= MAIN_PATTERN_SIDE_LEN || 
        c + cols >= MAIN_PATTERN_SIDE_LEN){
        throw RangeError();
    }
    
    for(var i = r; i < r + rows; i++){
        for(var k = c; k < c + cols; k++){
            if (pattern.getCell(i - r, k - c) === 1){
                mainPattern.setCell(i, k, pattern.getCell(i - r, k - c));
                drawCell(ctx, i, k, mainPattern.getCell(i, k));
            }
        }
    }
}